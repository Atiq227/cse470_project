    // Accept an order
    public function accept($id)
    {
        $order = Order::findOrFail($id);
        $order->update(['status' => 'accepted']);
        return response()->json(['message' => 'Order accepted'], 200);
    }

    // Decline an order
    public function decline($id)
    {
        $order = Order::findOrFail($id);
